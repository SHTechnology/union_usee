
public class LiveProgramData 
{
	String channelName = "";
	long lpBeginTime = 0;
	long lpEndTime = 0;
	String lpName = "";
	String lpDescription = "";
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	
	public String getLpName() {
		return lpName;
	}
	public void setLpName(String lpName) {
		this.lpName = lpName;
	}
	public String getLpDescription() {
		return lpDescription;
	}
	public void setLpDescription(String lpDescription) {
		this.lpDescription = lpDescription;
	}
	public long getLpBeginTime() {
		return lpBeginTime;
	}
	public void setLpBeginTime(long lpBeginTime) {
		this.lpBeginTime = lpBeginTime;
	}
	public long getLpEndTime() {
		return lpEndTime;
	}
	public void setLpEndTime(long lpEndTime) {
		this.lpEndTime = lpEndTime;
	}
	
}
